// Incluir incremento por 1 y menos 1

let puntaje = 5;
puntaje++; //retorna 5
puntaje--; //retorna 5

++puntaje; //retorna 6
--puntaje; //retorna 4

puntaje += 3; //retorna 8
puntaje -= 3; //retorna 2

console.log(puntaje);